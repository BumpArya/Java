package com.company; //Package groups function and classes -- usually associated with company and should be kept unique

public class Main { //Main --> Template for creating class objects

    public static void main(String[] args) { //main --> Function performs an action
	// write your code here --> This is a comment "//"
        System.out.println("Hello World"); // ln is used for new line

        int num1 = 1;
        int num2 = 2;
        int num3 = 3;
        int sum = num1 + num2 + num3 ;
        System.out.println(sum);
    }
}
// void --> means function will not return anything
// static --> makes it possible for us so that we can run this class function without making an object
// Public --> is an access modifier that allows class objects to be accessed anywhere

